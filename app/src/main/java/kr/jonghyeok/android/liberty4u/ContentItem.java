package kr.jonghyeok.android.liberty4u;

public class ContentItem {
    String name;
    int resId;

    public ContentItem(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getResId() {
        return resId;
    }

    public void setResId(int resId) {
        this.resId = resId;
    }
}
